              <p>
                CBD is one of the many cannabinoids found in the cannabis plant. Unlike its counterpart THC, CBD does
                not
                have psychoactive effects, meaning it won't make you feel "high." Instead, CBD is known for its
                potential
                therapeutic properties and can be derived from hemp, a variety of cannabis with low THC levels.
              </p>


